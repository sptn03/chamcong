CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- =========================================================
-- FUNCTION & TRIGGER ĐỂ TỰ ĐỘNG CẬP NHẬT updated_at
-- =========================================================
CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- =========================================================
-- 1. USERS (Tài khoản Đăng nhập Hệ thống)
-- =========================================================
CREATE TABLE users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  phone VARCHAR(20) NOT NULL UNIQUE,
  email VARCHAR(190) UNIQUE,
  pass VARCHAR(255),
  full_name TEXT NOT NULL,
  birthday DATE,
  gender SMALLINT CHECK (gender IN (1, 2, 3)),
  is_hunonic BOOLEAN NOT NULL DEFAULT FALSE,
  status SMALLINT NOT NULL DEFAULT 1 CHECK (status IN (1, 2)),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TRIGGER trg_users_updated_at BEFORE UPDATE ON users
FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- =========================================================
-- 2. COMPANIES (Công ty)
-- =========================================================
CREATE TABLE companies (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  code VARCHAR(60) NOT NULL,
  timezone VARCHAR(60) NOT NULL DEFAULT 'Asia/Ho_Chi_Minh',
  deleted_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TRIGGER trg_companies_updated_at BEFORE UPDATE ON companies
FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- =========================================================
-- 3. BRANCHES (Chi nhánh)
-- =========================================================
CREATE TABLE branches (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id INTEGER NOT NULL REFERENCES companies(id) ON DELETE RESTRICT,
  name TEXT NOT NULL,
  address TEXT,
  deleted_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT uq_branch_company UNIQUE (id, company_id)
);

CREATE TRIGGER trg_branches_updated_at BEFORE UPDATE ON branches
FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- =========================================================
-- 4. DEPARTMENTS (Phòng ban — thuộc chi nhánh hoặc cấp công ty)
-- =========================================================
CREATE TABLE departments (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id INTEGER NOT NULL REFERENCES companies(id) ON DELETE RESTRICT,
  branch_id INTEGER,
  name TEXT NOT NULL,
  deleted_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT uq_department_company UNIQUE (id, company_id),
  CONSTRAINT fk_department_branch_company FOREIGN KEY (branch_id, company_id) REFERENCES branches(id, company_id) ON DELETE RESTRICT
);

CREATE TRIGGER trg_departments_updated_at BEFORE UPDATE ON departments
FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- =========================================================
-- 5. EMPLOYEES (Hồ sơ Nhân viên từng Công ty)
-- =========================================================
CREATE TABLE employees (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  company_id INTEGER NOT NULL REFERENCES companies(id) ON DELETE RESTRICT,
  branch_id INTEGER NOT NULL,
  department_id INTEGER NOT NULL,
  employee_code VARCHAR(60) NOT NULL,
  title VARCHAR(120),
  status SMALLINT NOT NULL DEFAULT 1 CHECK (status IN (1, 2)),
  deleted_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT uq_employee_company UNIQUE (id, company_id),
  CONSTRAINT uq_employee_user_company UNIQUE (id, user_id, company_id),
  CONSTRAINT fk_employee_branch_company FOREIGN KEY (branch_id, company_id) REFERENCES branches(id, company_id) ON DELETE RESTRICT,
  CONSTRAINT fk_employee_department_company FOREIGN KEY (department_id, company_id) REFERENCES departments(id, company_id) ON DELETE RESTRICT
);

CREATE TRIGGER trg_employees_updated_at BEFORE UPDATE ON employees
FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- =========================================================
-- 6. COMPANY MEMBERSHIPS (Quyền của User trong từng Công ty)
-- =========================================================
CREATE TABLE company_memberships (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  company_id INTEGER NOT NULL REFERENCES companies(id) ON DELETE RESTRICT,
  employee_id BIGINT,
  role SMALLINT NOT NULL DEFAULT 2 CHECK (role IN (1, 2)),
  active_department_id INTEGER,
  deleted_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT fk_membership_employee_user_company FOREIGN KEY (employee_id, user_id, company_id) REFERENCES employees(id, user_id, company_id) ON DELETE CASCADE,
  CONSTRAINT fk_membership_dept_company FOREIGN KEY (active_department_id, company_id) REFERENCES departments(id, company_id) ON DELETE SET NULL,
  CONSTRAINT chk_membership_employee_required CHECK (role = 1 OR employee_id IS NOT NULL)
);

CREATE TRIGGER trg_company_memberships_updated_at BEFORE UPDATE ON company_memberships
FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- =========================================================
-- 7. DEVICES (Thiết bị đăng ký chấm công)
