-- =========================================================
-- 9. LOCATIONS (Danh mục Vị trí GPS Văn phòng)
-- =========================================================
CREATE TABLE locations (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id INTEGER NOT NULL REFERENCES companies(id) ON DELETE RESTRICT,
  branch_id INTEGER NOT NULL,
  employee_id BIGINT REFERENCES employees(id) ON DELETE CASCADE,
  name TEXT NOT NULL,
  address TEXT,
  lat NUMERIC(10,7) NOT NULL CHECK (lat BETWEEN -90 AND 90),
  lng NUMERIC(10,7) NOT NULL CHECK (lng BETWEEN -180 AND 180),
  radius_m INTEGER NOT NULL DEFAULT 80 CHECK (radius_m > 0),
  deleted_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT uq_location_company UNIQUE (id, company_id),
  CONSTRAINT fk_location_branch_company FOREIGN KEY (branch_id, company_id) REFERENCES branches(id, company_id) ON DELETE RESTRICT
);

CREATE TRIGGER trg_locations_updated_at BEFORE UPDATE ON locations
FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- =========================================================
