CREATE TRIGGER trg_devices_updated_at BEFORE UPDATE ON devices
FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- =========================================================
-- 8. TOKENS (Quản lý Phiên đăng nhập & Token xác thực)
